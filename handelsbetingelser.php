	<h1>Bestilling</h1>
	<p>        
		Bestilling sker ved aftale mellem Crooked Films og køber. En bindende aftale mellem køber og Crooked Films er først indgået, når Crooked Films har påtaget sig opgaven og fremsendt en ordrebekræftelse.
	</p>
	
	<h1>Priser</h1>
	<p>      
		Prisen du finder på vores hjemmeside er en grundpris og dækker ideudvikling, manuskript, storyboard, forberedelse til optagelse, optagelse, redigering og levering af færdige film. 
Køber bliver faktureret til den pris, der er gældende på bestillingsdagen. 

Andre omkostninger såsom transport, speak, skuespillere, modeller, stunts, ekstra udgifter til location f.eks. ved leje af studie mm., er ikke inkluderet i grundprisen, da disse varierer fra projekt til projekt. Priserne på disse ekstra omkostninger vil tydeligt fremgå på fremsendte faktura, når aftale er indgået.

Alle priser er angivet inkl. Moms
	</p>
	
	<h1>Betaling</h1>
	<p>
		Betalingen er opdelt i to rater. Første faktura fremsendes sammen med ordrebekræftelsen og lyder på halvdelen af standardprisen. Anden faktura fremsendes sammen med færdig film og lyder på resten af standardprisen og eventuelle ekstra omkostninger. 

Betaling kan ske enten via bankoverførelse eller indbetaling af girokort. Kontooplysninger til brug ved betaling vil fremgå af ordrebekræftelse. 

Betales der ikke inden for betalingsbetingelserne på netto 8 dage, pålægges rykkergebyr hver 10. dag efter de gældende regler.
	</p>
	
	<h1>Levering og installation</h1>
	<p>
		Køber har pligt til efter modtagelse, at sikre at filen virker som den skal. Er der problemer kontaktes Crooked Films, så dette kan blive ordnet. Ved beskadigelse eller tab af selve filen vil Crooked Films fremsende denne igen uden ekstra omkostninger.
	</p>
	
	<h1>Ansvarsfraskrivelse</h1>
	<p>
		På www.crooked-films.com kan forekomme links til andre hjemmesider, herunder links til vores kunders hjemmesider. Alle disse er uden for Crooked Films kontrol og vi kan derfor på ingen måde drages til ansvar for indholdet på disse sider. Vi kan heller ikke drages til ansvar for indholdet på steder hvor vores kunder vælger at postere deres film.
	</p>
	
	<h1>Ophavsret</h1>
	<p>
		Køber har ret til at bruge filmen ubegrænset, sådan som den er leveret af Crooked Films. Crooked Films ejer ophavsretten, hvorfor køber eller andre ikke må ændre i eller kopiere filmen uden tilladelse fra Crooked Films.
	</p>
	
